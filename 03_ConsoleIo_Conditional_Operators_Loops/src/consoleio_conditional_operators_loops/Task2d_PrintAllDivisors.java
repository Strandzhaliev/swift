
package consoleio_conditional_operators_loops;

import java.util.Scanner;

public class Task2d_PrintAllDivisors {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = 1; i <= n; i++) {
            if (n % i == 0) {
                System.out.print(i + " ");
            }
        }
    }
}
